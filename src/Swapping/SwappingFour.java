package Swapping;

public class SwappingFour {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;
        int c = 5;
        int d = 6;
        int temp = a;
        a = b;
        b = c;
        c = d;
        d = temp;
        System.out.println("Value of A : " + a);
        System.out.println("Value of B : " + b);
        System.out.println("Value of C : " + c);
        System.out.println("Value of D : " + d);
    }
}

